using Contact_Prj.Context;
using Microsoft.EntityFrameworkCore;

namespace Contact_Prj
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.
            builder.Services.AddControllersWithViews();

            builder.Services.AddDbContext<ContactsDbContext>(options =>
            options.UseSqlServer(builder.Configuration.GetConnectionString("ContactConnectionString") ?? throw new InvalidOperationException("Connection string 'ContactConnectionString' not found.")));


            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (!app.Environment.IsDevelopment())
            {
                app.UseExceptionHandler("/Home/Error");
            }
            app.UseStaticFiles();

            app.UseRouting();

            app.UseAuthorization();

            app.MapControllerRoute(
                name: "default",
                pattern: "{controller=Home}/{action=Index}/{id?}");

            app.Run();
        }
    }
}
